    select corpus.id,
           lower(coalesce(meta.title, '') || coalesce(meta.abstract, '')) text
    from oecd.en_2010_2020 corpus
             inner join gcp_cset_links_v2.all_metadata_with_cld2_lid meta on meta.id = corpus.id
)
