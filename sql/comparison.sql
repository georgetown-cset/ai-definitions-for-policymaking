       all_categories.wos_id,
       all_categories.ds_id,
       all_categories.mag_id,
       years.year,
       all_countries.country,
       all_categories.wos_subject,
       all_categories.ds_subject,
       all_categories.mag_subject,
       citation_counts.min_times_cited,
       citation_counts.times_cited,
       citation_counts.ds_times_cited,
       citation_counts.wos_times_cited,
       citation_counts.mag_times_cited,
       all_predictions.arxiv_coverage,
       all_predictions.keyword_hit,
       all_predictions.elsevier_hit,
       all_predictions.subject_hit,
       all_predictions.scibert_hit,
       all_predictions.scibert_cl_hit,
       all_predictions.scibert_cv_hit,
       all_predictions.scibert_ro_hit,
       all_predictions.scibert_not_cv_hit,
       all_predictions.arxiv_scibert_hit,
       all_predictions.arxiv_scibert_cl_hit,
       all_predictions.arxiv_scibert_cv_hit,
       all_predictions.arxiv_scibert_ro_hit,
       all_predictions.arxiv_scibert_not_cv_hit,
       percentiles.percentile,
       percentiles.keyword_percentile,
       percentiles.elsevier_percentile,
       percentiles.subject_percentile,
       percentiles.scibert_percentile,
       percentiles.scibert_cv_percentile,
       percentiles.scibert_cl_percentile,
       percentiles.scibert_ro_percentile,
       percentiles.scibert_not_cv_percentile,
       percentiles.arxiv_scibert_percentile,
       percentiles.arxiv_scibert_cv_percentile,
       percentiles.arxiv_scibert_cl_percentile,
       percentiles.arxiv_scibert_ro_percentile,
       percentiles.arxiv_scibert_not_cv_percentile,
       -- Minimum-citation percentiles
       percentiles.min_percentile,
       percentiles.keyword_min_percentile,
       percentiles.elsevier_min_percentile,
       percentiles.scibert_min_percentile,
       percentiles.scibert_cv_min_percentile,
       percentiles.scibert_cl_min_percentile,
       percentiles.scibert_ro_min_percentile,
