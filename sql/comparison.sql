       wos_percentiles.wos_arxiv_scibert_ro_percentile,
       wos_percentiles.wos_arxiv_scibert_not_cv_percentile
from oecd.all_years years
         left join oecd.all_citation_counts citation_counts
                   on citation_counts.cset_id = years.cset_id
         left join oecd.all_predictions all_predictions on all_predictions.cset_id = years.cset_id
         left join oecd.all_countries all_countries on all_countries.cset_id = years.cset_id
         left join oecd.percentiles on percentiles.cset_id = years.cset_id
         left join oecd.all_categories on all_categories.cset_id = years.cset_id
         left join oecd.ds_percentiles on ds_percentiles.cset_id = years.cset_id
         left join oecd.mag_percentiles on mag_percentiles.cset_id = years.cset_id
         left join oecd.wos_percentiles on wos_percentiles.cset_id = years.cset_id
